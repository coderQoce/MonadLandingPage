export const getAppsByCategory = (categoryId) => {
  if (categoryId === 'all') return ecosystemApps;
  const category = categories.find(c => c.id === categoryId);
  return category ? ecosystemApps.filter(app => app.category.toLowerCase() === category.name.toLowerCase()) : [];
};

export const searchApps = (query) => {
  if (!query) return ecosystemApps;
  const q = query.toLowerCase();
  return ecosystemApps.filter(app =>
    app.name.toLowerCase().includes(q) ||
    app.description.toLowerCase().includes(q) ||
    app.category.toLowerCase().includes(q)
  );
};

